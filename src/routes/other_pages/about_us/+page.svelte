    <section class="portfolio-section">
        <h2 class="section-title">Focus sur une technologie</h2>
        <div class="text-block">
            <p class="question-box"><strong>Ma question de veille :</strong> Quelle est la place de l’IA dans le milieu de la conception du jeu vidéo ?</p>
            
            <p>À l'ère d'une IA qui révolutionne nos vies — qu'il s'agisse de ChatGPT pour nous épauler sur nos rédactions ou des dernières versions connectées de Gemini avec son interface moderne — sa montée en puissance questionne le jeu vidéo. Pourtant, si le sujet explose publiquement depuis 2021, l'IA s'est installée dans ce secteur il y a belle lurette. En 1972, <em>Pong</em> intégrait déjà une IA pour animer sa raquette.</p>
            
            <p>Aujourd'hui, les IA génèrent des environnements complets ou des personnages, représentant environ 5% de la production totale. Les projections estiment que cette part pourrait monter à 50%, libérant du temps pour que les développeurs se concentrent sur des décisions créatives majeures. Côté expérience, elle promet une immersion folle avec des PNJ aux réactions profondément adaptées à nos choix, un axe dont le jeu <em>Detroit: Become Human</em> donnait déjà un avant-goût.</p>
            
            <p>Néanmoins, des zones d'ombre subsistent. Sur le plan créatif, l'IA ne fait que "recycler" des bases de données préexistantes. Cela pose la question cruciale du droit d'auteur : qui possède l'œuvre ? Le studio, l'artiste d'origine, ou le concepteur de l'IA ?</p>
            
            <p>Enfin, l'IA s'invite aussi côté matériel (hardware) via l'<em>upscaling</em> artificiel, qui sublime la qualité d'image tout en optimisant la fluidité pour les joueurs. Une chose est sûre, le jeu vidéo a de belles années devant lui, et l'IA, utilisée à bon escient, le propulsera au niveau supérieur pour les prochaines générations, à l'image du très attendu <em>Project 007</em> de IO Interactive.</p>
        </div>
